class Solution:
    # @param A : tuple of integers
    # @param B : integer
    # @return a list of integers
    def searchRange(self, A, B):
        if(len(A)==0):
                return [-1, -1]
        elif(len(A)==1):
                if(A[0]==B):
                    return [0,0]
                else:
                    return [-1,-1]
                
        def loc_left(low,high):
            if(low == high):
                if(A[low]==B):
                    return low
            elif(high == low+1):
                if(A[low]==B):
                    return low
                elif(A[high] == B):
                    return high
            else:
                mid = (low+high)//2
                if(A[mid]==B):
                    return mid
                elif(A[mid]>B):
                    return loc_left(low,mid-1)
                else:
                    return loc_left(mid+1,high)
            
        left_index  =  loc_left(0,len(A)-1)
        if(left_index == None):
            return [-1,-1]
        right_index = left_index
        while(A[left_index-1]==B):
            if(left_index==0):
                break
            left_index = left_index - 1
        while(A[right_index]==B):
            right_index = right_index+1
            if(right_index == len(A)):
                break
       
        return [left_index,right_index-1]
          
        
           
                    
                    
                






